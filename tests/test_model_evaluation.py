class TestIntegration:
    """Integration tests that combine multiple components."""

    def test_confusion_matrix_matches_metrics(self):
        """ConfusionMatrix accuracy should match MetricsCalculator.accuracy."""
        y_true = [1, 0, 1, 1, 0, 0, 1, 0]
        y_pred = [1, 0, 0, 1, 1, 0, 1, 0]
        cm = ConfusionMatrix(y_true, y_pred)
        mc_acc = MetricsCalculator.accuracy(y_true, y_pred)
        assert abs(cm.accuracy() - mc_acc) < 1e-9

    def test_full_pipeline(self):
        """End-to-end: create data, cross-validate, check all metrics exist."""
        X, y = make_binary_dataset(100)
        cv = CrossValidator(k=5, shuffle=True, random_seed=42)
        results = cv.cross_validate(
            DummyClassifier, {"strategy": "most_frequent"}, X, y, metric="accuracy"
        )
        assert len(results["scores"]) == 5
        assert 0 <= results["mean"] <= 1.0
        assert results["std"] >= 0

    def test_roc_auc_reasonable(self):
        """For well-separated data, AUC should be high (> 0.8)."""
        # Create clearly separable scores
        y_true = [1] * 50 + [0] * 50
        rng = random.Random(42)
        # Class 1 gets high scores, class 0 gets low scores
        y_scores = [rng.uniform(0.6, 1.0) for _ in range(50)] + [
            rng.uniform(0.0, 0.4) for _ in range(50)
        ]
        roc = MetricsCalculator.roc_curve(y_true, y_scores)
        auc_val = MetricsCalculator.auc(roc["fpr"], roc["tpr"])
        assert auc_val > 0.8, f"AUC {auc_val} too low for separable data"

    def test_metrics_on_random_data(self):
        """For random data, AUC should be near 0.5."""
        rng = random.Random(99)
        y_true = [rng.randint(0, 1) for _ in range(200)]
        y_scores = [rng.random() for _ in range(200)]
        roc = MetricsCalculator.roc_curve(y_true, y_scores)
        auc_val = MetricsCalculator.auc(roc["fpr"], roc["tpr"])
        assert 0.3 < auc_val < 0.7, f"AUC {auc_val} not near 0.5 for random data"

    def test_cross_validator_with_shuffling(self):
        """Shuffled CV should produce different fold assignments than unshuffled."""
        X = list(range(50))
        y = [i % 2 for i in range(50)]
        cv_shuffled = CrossValidator(k=5, shuffle=True, random_seed=42)
        cv_unshuffled = CrossValidator(k=5, shuffle=False)
        folds_s = list(cv_shuffled.split(X, y))
        folds_u = list(cv_unshuffled.split(X, y))
        # At least one fold should have different validation data
        any_different = False
        for (_, _, vs_X, _), (_, _, vu_X, _) in zip(folds_s, folds_u):
            if vs_X != vu_X:
                any_different = True
                break
        assert any_different, "Shuffled and unshuffled should produce different folds"
