# Helper: a simple dummy classifier for cross-validation tests
# ---------------------------------------------------------------------------

class DummyClassifier:
    """A dummy classifier that always predicts the most frequent label."""

    def __init__(self, strategy="most_frequent"):
        self.strategy = strategy
        self._prediction = None

    def fit(self, X, y):
        if self.strategy == "most_frequent":
            counts = {}
            for label in y:
                counts[label] = counts.get(label, 0) + 1
            self._prediction = max(counts, key=counts.get)
        return self

    def predict(self, X):
        return [self._prediction] * len(X)


# ---------------------------------------------------------------------------
# Helper: generate a simple binary classification dataset
# ---------------------------------------------------------------------------

def make_binary_dataset(n=100, seed=42):
    """Create a simple binary dataset where X > 0.5 tends to be class 1."""
    rng = random.Random(seed)
    X = []
    y = []
    for _ in range(n):
        x_val = rng.random()
        noise = rng.gauss(0, 0.1)
        label = 1 if (x_val + noise) > 0.5 else 0
        X.append([x_val])
        y.append(label)
    return X, y


