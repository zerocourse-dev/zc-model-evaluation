class TestMetrics:
    """Tests for the MetricsCalculator class."""

    def test_accuracy(self):
        """Accuracy for simple known values."""
        y_true = [1, 0, 1, 1, 0]
        y_pred = [1, 0, 0, 1, 0]
        # 4 correct out of 5
        assert MetricsCalculator.accuracy(y_true, y_pred) == 0.8

    def test_precision_recall(self):
        """Precision and recall for a specific case."""
        y_true = [1, 1, 0, 0, 1, 0]
        y_pred = [1, 0, 0, 1, 1, 0]
        # TP=2 (idx 0,4), FP=1 (idx 3), FN=1 (idx 1), TN=2 (idx 2,5)
        # precision = 2/3, recall = 2/3
        assert abs(MetricsCalculator.precision(y_true, y_pred) - 2 / 3) < 1e-9
        assert abs(MetricsCalculator.recall(y_true, y_pred) - 2 / 3) < 1e-9

    def test_mse(self):
        """MSE for [1,2,3] vs [1.1, 2.1, 2.9] is close to 0.01."""
        y_true = [1, 2, 3]
        y_pred = [1.1, 2.1, 2.9]
        mse = MetricsCalculator.mean_squared_error(y_true, y_pred)
        assert abs(mse - 0.01) < 1e-6

    def test_r_squared_perfect(self):
        """R-squared is 1.0 for identical predictions."""
        y_true = [1, 2, 3, 4, 5]
        y_pred = [1, 2, 3, 4, 5]
        assert MetricsCalculator.r_squared(y_true, y_pred) == 1.0

    def test_r_squared_range(self):
        """R-squared should be <= 1.0."""
        y_true = [1, 2, 3, 4, 5]
        y_pred = [1.1, 2.2, 2.8, 4.1, 5.0]
        r2 = MetricsCalculator.r_squared(y_true, y_pred)
        assert r2 <= 1.0

    def test_roc_curve_keys(self):
        """ROC curve returns dict with correct keys."""
        y_true = [1, 0, 1, 0]
        y_scores = [0.9, 0.1, 0.8, 0.2]
        result = MetricsCalculator.roc_curve(y_true, y_scores)
        assert "thresholds" in result
        assert "tpr" in result
        assert "fpr" in result
        assert len(result["tpr"]) == len(result["fpr"])
        assert len(result["tpr"]) == len(result["thresholds"])

    def test_auc_perfect(self):
        """AUC for a perfect classifier: TPR=[0,1,1], FPR=[0,0,1] => 1.0."""
        fpr = [0, 0, 1]
        tpr = [0, 1, 1]
        auc_val = MetricsCalculator.auc(fpr, tpr)
        assert abs(auc_val - 1.0) < 1e-9


