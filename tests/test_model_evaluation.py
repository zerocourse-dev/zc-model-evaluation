class TestLearningCurve:
    """Tests for the LearningCurve class."""

    def test_compute_returns_dict(self):
        """compute() returns dict with 'train_sizes', 'train_scores', 'val_scores'."""
        X, y = make_binary_dataset(100)
        lc = LearningCurve(DummyClassifier, {"strategy": "most_frequent"}, X, y)
        results = lc.compute([30, 60, 90], metric="accuracy", k=3)
        assert "train_sizes" in results
        assert "train_scores" in results
        assert "val_scores" in results

    def test_train_sizes_match(self):
        """Output train_sizes matches the input sizes."""
        X, y = make_binary_dataset(100)
        lc = LearningCurve(DummyClassifier, {"strategy": "most_frequent"}, X, y)
        sizes = [20, 40, 60, 80]
        results = lc.compute(sizes, k=3)
        assert results["train_sizes"] == sizes

    def test_scores_length(self):
        """train_scores and val_scores have same length as train_sizes."""
        X, y = make_binary_dataset(100)
        lc = LearningCurve(DummyClassifier, {"strategy": "most_frequent"}, X, y)
        sizes = [20, 40, 60]
        results = lc.compute(sizes, k=3)
        assert len(results["train_scores"]) == len(sizes)
        assert len(results["val_scores"]) == len(sizes)

    def test_more_data_generally_helps(self):
        """With more training data, validation scores should generally not collapse.

        For a dummy classifier on balanced-ish data, scores should remain
        reasonable (> 0.3) at all training sizes.
        """
        X, y = make_binary_dataset(200, seed=123)
        lc = LearningCurve(DummyClassifier, {"strategy": "most_frequent"}, X, y)
        results = lc.compute([30, 60, 120, 180], metric="accuracy", k=3)
        for score in results["val_scores"]:
            assert score > 0.3, f"Validation score {score} is unreasonably low"


