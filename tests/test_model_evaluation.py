class TestCrossValidator:
    """Tests for the CrossValidator class."""

    def test_split_count(self):
        """k=5 yields exactly 5 folds."""
        X = list(range(100))
        y = [i % 2 for i in range(100)]
        cv = CrossValidator(k=5)
        folds = list(cv.split(X, y))
        assert len(folds) == 5

    def test_split_no_overlap(self):
        """Validation sets across folds should not overlap."""
        X = list(range(50))
        y = [0] * 50
        cv = CrossValidator(k=5, shuffle=False)
        val_indices_all = []
        for train_X, train_y, val_X, val_y in cv.split(X, y):
            val_indices_all.extend(val_X)
        # Since X contains the indices themselves and shuffle=False,
        # the validation sets should have no duplicates
        assert len(val_indices_all) == len(set(val_indices_all))

    def test_split_covers_all(self):
        """Union of all validation sets should cover all data points."""
        X = list(range(50))
        y = [0] * 50
        cv = CrossValidator(k=5, shuffle=False)
        val_all = []
        for train_X, train_y, val_X, val_y in cv.split(X, y):
            val_all.extend(val_X)
        assert sorted(val_all) == list(range(50))

    def test_fold_sizes(self):
        """Each fold should have roughly the same validation size."""
        X = list(range(100))
        y = [0] * 100
        cv = CrossValidator(k=5, shuffle=False)
        sizes = []
        for train_X, train_y, val_X, val_y in cv.split(X, y):
            sizes.append(len(val_X))
        # With 100 items and k=5, each fold should have exactly 20
        assert all(s == 20 for s in sizes)

    def test_cross_validate_returns_dict(self):
        """cross_validate returns a dict with 'scores', 'mean', 'std'."""
        X, y = make_binary_dataset(100)
        cv = CrossValidator(k=5)
        results = cv.cross_validate(DummyClassifier, {"strategy": "most_frequent"}, X, y)
        assert "scores" in results
        assert "mean" in results
        assert "std" in results

    def test_cross_validate_score_count(self):
        """Number of scores equals k."""
        X, y = make_binary_dataset(100)
        cv = CrossValidator(k=5)
        results = cv.cross_validate(DummyClassifier, {"strategy": "most_frequent"}, X, y)
        assert len(results["scores"]) == 5

    def test_cross_validate_deterministic(self):
        """Same seed produces same results."""
        X, y = make_binary_dataset(100)
        cv1 = CrossValidator(k=5, shuffle=True, random_seed=42)
        cv2 = CrossValidator(k=5, shuffle=True, random_seed=42)
        r1 = cv1.cross_validate(DummyClassifier, {"strategy": "most_frequent"}, X, y)
        r2 = cv2.cross_validate(DummyClassifier, {"strategy": "most_frequent"}, X, y)
        assert r1["scores"] == r2["scores"]

    def test_cross_validate_mean_std(self):
        """Mean and std are correctly computed from scores."""
        X, y = make_binary_dataset(100)
        cv = CrossValidator(k=5)
        results = cv.cross_validate(DummyClassifier, {"strategy": "most_frequent"}, X, y)
        scores = results["scores"]
        expected_mean = sum(scores) / len(scores)
        expected_std = math.sqrt(sum((s - expected_mean) ** 2 for s in scores) / len(scores))
        assert abs(results["mean"] - expected_mean) < 1e-9
        assert abs(results["std"] - expected_std) < 1e-9


